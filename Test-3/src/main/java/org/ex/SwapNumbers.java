package org.ex;

public class SwapNumbers {
    //8. Write a program to swap the two numbers.
        public static void main(String[] args) {
            int x=10,y=20,z=0;
            System.out.println("X before changes"+x);
            System.out.println("y before changes"+y);
            z=x;
            x=y;
            y=z;
            System.out.println("X after changes"+x);
            System.out.println("y after changes "+y);
        }
    }

